// In this file, we practice three important types of operators in JavaScript:
// 1. Arithmetic Operators: for mathematical calculations (addition, subtraction, multiplication, division, etc.)
// 2. Comparison Operators: for comparing values (equality, greater than, less than, etc.)
// 3. Logical Operators: for combining conditions (AND, OR, NOT)

let a = 10;
let b = 5;

// ------- Arithmetic Operators -------
// Use arithmetic operators (+, -, *, /) to calculate and print sum, difference, product, and quotient of two numbers
console.log("Sum:", a + b);
console.log("Difference:", a - b);
console.log("Product:", a * b);
console.log("Quotient:", a / b);



// ------- Comparison Operators -------
// Use comparison operators (==, ===, >, <, !=, !==, >=, <=) to compare numbers
console.log(5 == "5");   // true   
console.log(5 === "5");  // false  
console.log(7 > 3);      // true
console.log(7 < 3);      // false
console.log(5 != 6);     // true
console.log(5 !== "5");  // true
console.log(13 >= 13);  // true
console.log(13 <= 12);  // false



// ------- Logical Operators -------
// Use logical operators (&&, ||, !) to combine conditions
let x = 13;

console.log(x > 5 && x < 13);
console.log(x > 5 || x <20);
console.log(!(x == 13));


