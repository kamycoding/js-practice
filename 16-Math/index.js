// Math Examples

let number = 13;
// EN: Declares a variable 'number' and assigns the value 13
// DE: Deklariert eine Variable 'number' mit dem Wert 13

console.log(number ** 2);
// EN: Outputs the square of 'number' (13 squared equals 169)
// DE: Gibt das Quadrat von 'number' aus (13² = 169)

console.log(Math.pow(13, 3));
// EN: Outputs 13 to the power of 3 (13³ equals 2197)
// DE: Gibt 13 hoch 3 aus (13³ = 2197)

console.log(Math.sqrt(64));
// EN: Outputs the square root of 64 (equals 8)
// DE: Gibt die Quadratwurzel von 64 aus ( = 8)

console.log(Math.abs(-3));
// EN: Outputs the absolute value of -3 (equals 3)
// DE: Gibt den Absolutwert von -3 aus ( = 3)

console.log(Math.min(12, 13, 14, 3, 2, 99));
// EN: Outputs the smallest number among the arguments (2)
// DE: Gibt die kleinste Zahl unter den Argumenten aus (2)

let maxNumber = Math.max(12, 13, 14, 3, 2, 99);
// EN: Finds the largest number among the arguments and stores it in 'maxNumber'
// DE: Ermittelt die größte Zahl unter den Argumenten und speichert sie in 'maxNumber'

console.log(maxNumber);
// EN: Outputs the value of 'maxNumber' (99)
// DE: Gibt den Wert von 'maxNumber' aus (99)

console.log(Math.floor(13.3));
// EN: Rounds 13.3 down to the nearest integer (13)
// DE: Rundet 13.3 auf die nächstkleinere Ganzzahl ab (13)

console.log(Math.trunc(13.333));
// EN: Truncates the decimal part of 13.333 (13)
// DE: Entfernt den Dezimalteil von 13.333 (13)

console.log(Math.round(13.3));
// EN: Rounds 13.3 to the nearest integer (13)
// DE: Rundet 13.3 auf die nächste Ganzzahl (13)

console.log(Math.round(13.6));
// EN: Rounds 13.6 to the nearest integer (14)
// DE: Rundet 13.6 auf die nächste Ganzzahl (14)

console.log(Math.ceil(13.3));
// EN: Rounds 13.3 up to the nearest integer (14)
// DE: Rundet 13.3 auf die nächsthöhere Ganzzahl auf (14)

console.log(Math.random());
// EN: Outputs a random number between 0 (inclusive) and 1 (exclusive)
// DE: Gibt eine Zufallszahl zwischen 0 (inklusive) und 1 (exklusive) aus

console.log(Math.floor(Math.random() * 200));
// EN: Generates a random integer between 0 and 199 by multiplying a random number by 200 and rounding down
// DE: Erzeugt eine Zufallszahl zwischen 0 und 199, indem eine Zufallszahl mit 200 multipliziert und abgerundet wird


